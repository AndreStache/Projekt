package vorlesungsplan.pd;

public class TableItem {
    public String block;
    public String mon;
    public String die;
    public String mit;
    public String don;
    public String fre;

    public String getMon() {
        return mon;
    }

    public void setMon(String mon) {
        this.mon = mon;
    }

    public String getDie() {
        return die;
    }

    public void setDie(String die) {
        this.die = die;
    }

    public String getMit() {
        return mit;
    }

    public void setMit(String mit) {
        this.mit = mit;
    }

    public String getDon() {
        return don;
    }

    public void setDon(String don) {
        this.don = don;
    }

    public String getFre() {
        return fre;
    }

    public void setFre(String fre) {
        this.fre = fre;
    }

    public String getBlock() {
        return block;
    }

    public void setBlock(String block) {
        this.block = block;
    }
}
